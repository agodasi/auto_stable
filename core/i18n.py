def set_language(lang_code):
    global _current_lang
    if lang_code in TRANSLATIONS:
        _current_lang = lang_code

def t(key, **kwargs):
    text = TRANSLATIONS.get(_current_lang, TRANSLATIONS["ja"]).get(key, key)
    if kwargs:
        return text.format(**kwargs)
    return text
